def convert_to_trainID(img_path):
    img = Image.open(img_path)
    pixels = img.load() # create the pixel map
    out = np.zeros(shape = (img.size[1],img.size[0]))
    error = np.zeros(shape = (img.size[1],img.size[0],3))
    errorpix = 0
    for i in range(img.size[0]): # height
        for j in range(img.size[1]): # width
            color = (pixels[i,j][0],pixels[i,j][1],pixels[i,j][2])
            try:
                color_id = colortotrainId[color].trainId
                out[j][i] = color_id
            except KeyError:
                errorpix = errorpix + 1
                correction = find_nearest_color(color)
                error[j,i,:] = correction
                color_id = colortotrainId[correction].trainId
                out[j][i] = color_id
                #print('color doesnt exist',color)
    print(errorpix)
    #save photo
    out_im = Image.fromarray(out)
    out_im = out_im.convert("L")

    error_im = Image.fromarray(error.astype('uint8'))
    return out_im, error_im

def find_nearest_color(color_point):
    closest_colors = sorted(labels, key=lambda label: color_distance(label.color, color_point))
    closest_color = closest_colors[0].color
    return closest_color

def color_distance(c1, c2):
    (r1,g1,b1) = c1
    (r2,g2,b2) = c2
    return math.sqrt((r1 - r2)**2 + (g1 - g2) ** 2 + (b1 - b2) **2)

